def ReadFasta(Data):
    fastaDict = {}
    label = None
    for line in Data.strip().splitlines():
        #strip(): remove any leading and trailing whitespace from the entire string.
        #splitlines(): cleaned string into a list of lines, based on the newline characters(\n),
        #              and returns them as a list of strings.
        line = line.strip()
        if line.startswith('>'):
            label = line[1:]
            fastaDict[label] = ''
        else:
            fastaDict[label] += line
    return fastaDict 

def GCContent(seq):
    GCCount = seq.count('G') + seq.count('C')
    GCPercent = round(GCCount / len(seq) * 100, 6)
    return GCPercent

def HighestGCContent(Data):
    fastaDict = ReadFasta(Data)
    maxLabel = None
    maxGC = 0
    for label, seq in fastaDict.items():
        GC = GCContent(seq)
        if GC > maxGC:
            maxGC = GC
            maxLabel = label
    return maxLabel, maxGC
